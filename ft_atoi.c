#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	cont;
	size_t	cifra;

	i = 0;
	cont = 1;
	cifra = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		cont = -cont;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		cifra = cifra * 10 + (str[i] - '0');
		i++;
	}
	return (cifra * cont);
}
