#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	char	*ret;
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	len = ft_strlen(s);
	ret = malloc (sizeof (char) * len + 1);
	if (!ret)
		return (NULL);
	while (s[i] != '\0')
	{
		ret[i] = f(i, s[i]);
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
